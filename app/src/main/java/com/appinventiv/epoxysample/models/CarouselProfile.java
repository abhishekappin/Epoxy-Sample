package com.appinventiv.epoxysample.models;

public class CarouselProfile {

    String title;
    String description;

    public CarouselProfile(String title, String description) {
        this.title = title;
        this.description = description;
    }

    public String getTitle() {
        return title;
    }

    public String getDescription() {
        return description;
    }
}
